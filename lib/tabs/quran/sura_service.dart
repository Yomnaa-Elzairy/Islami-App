  static List<Sura> searchResults =
      List.generate(arabicSuraName.length, (index) => getSuraFromIndex(index));
  static List<Sura> mostRecently = [];
  static Sura getSuraFromIndex(int index) => Sura(
      arabicSuraName: arabicSuraName[index],
      englishSuraName: englishSuraName[index],
      ayatNumber: ayaNumber[index],
      num: index + 1);

  static Future<void> getMostRecently() async {
    List<String>? mostRecentIndex =
        LocalStorage.getStringList("mostRecentIndexes");

    if (mostRecentIndex == null) return;
    mostRecently = mostRecentIndex.map((stringIndex) {
      int index = int.parse(stringIndex);
      Sura sura = getSuraFromIndex(index);
      return sura;
    }).toList();
  }

  static Future<void> addSuraToMostRecently(Sura sura) async {
    bool isFound =
        mostRecently.any((mostRecentSura) => mostRecentSura.num == sura.num);
    if (!isFound) {
      mostRecently.add(sura);
      List<String> mostrecentindexes =
          mostRecently.map((sura) => (sura.num - 1).toString()).toList();
      LocalStorage.setStringList("mostRecentIndexes", mostrecentindexes);
    }

    // mostRecently.contains(sura) ?  : mostRecently.add(sura);
  }

  static void search(String query) {
    SuraService.searchResults.clear();
    for (int i = 0; i < arabicSuraName.length; i++) {
      if (arabicSuraName[i].contains(query) ||
          englishSuraName[i].toLowerCase().contains(query.toLowerCase())) {
        searchResults.add(getSuraFromIndex(i));
      }
    }
  }
